    private boolean validata(String telefonnumarasi,String tuta) {
       
        Pattern p = Pattern.compile("^5+[0-9]{2}[0-9]{3}[0-9]{2}[0-9]{2}");
        Matcher matcher = p.matcher(telefonnumarasi);
        if(!matcher.matches())
            return false;
        try {
            Float.parseFloat(tuta);
        }catch (Exception ex) {
            return false;
        }
       return true;
       }
