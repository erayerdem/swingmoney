    private void referanslarıbul(String telno,float yenitutar) {
        try {
            PreparedStatement st = connection.prepareStatement("select referansolan as pd from musteri where numara=?");
            st.setString(1, telno);
            ResultSet executeQuery = st.executeQuery();
            int ilkref=0;
            if(executeQuery.next()) {
                ilkref=executeQuery.getInt("pd");
                
            }
            st.close();
            executeQuery.close();
            
            PreparedStatement st1 = connection.prepareStatement("select referansolan as pd from musteri where referansno=?");
            st1.setInt(1, ilkref);
            ResultSet executeQuery1 = st1.executeQuery();
            int ikinciref=0;
            if(executeQuery1.next()) {
                ikinciref=executeQuery1.getInt("pd");
                
            }
            st1.close();
            executeQuery1.close();


            PreparedStatement st2= connection.prepareStatement("select referansolan as pd from musteri where referansno=?");
            st2.setInt(1, ikinciref);
            ResultSet executeQuery2 = st2.executeQuery();
            int ucuncuref=0;
            if(executeQuery2.next()) {
                ucuncuref=executeQuery2.getInt("pd");
                
            }
            st2.close();
            executeQuery2.close();    
            System.out.println(ilkref+" "+ikinciref+" "+ucuncuref);
            if(ilkref!=0) {
                updateparapuanwithrefnum(yenitutar,ilkref);
                if(ikinciref!=0) {
                    updateparapuanwithrefnum(yenitutar,ikinciref);
                    if(ucuncuref!=0) {
                    updateparapuanwithrefnum(yenitutar,ucuncuref);
                }
                }
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Odemeyap.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }
