    private boolean  isexist (String phonenumber)  {
        
        try {
            PreparedStatement prepareStatement = connection.prepareStatement("select * from musteri where numara=?");
            
            prepareStatement.setString(1, phonenumber);
            
            ResultSet executeQuery = prepareStatement.executeQuery();
            if(executeQuery.next()) {
                return true;
            }
            executeQuery.close();
             prepareStatement.close();
            return false;
        } catch (SQLException ex) {
            Logger.getLogger(Odemeyap.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }
